    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        rpt = JOptionPane.showConfirmDialog(null, "Seguro que desea Registrar?");

        if (rpt == JOptionPane.YES_OPTION) {
            rBean.setCiudadOrigen(lblCO.getText());
            rBean.setCiudadDestino(lblCD.getText());
            rBean.setTipoCircuito(cbTipoC.getSelectedItem().toString());
            rBean.setDescripcion(txtDescripcion.getText());

            rDao.registrar_Mant_Ruta(rBean);
            rDao.ListarMantenimientoRutas();
        } else if (rpt == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "No sea realizado ningun Registro!");
        } else if (rpt == JOptionPane.CANCEL_OPTION) {
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void tblMantRutasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMantRutasMouseClicked
        for (int i = 0; i < tblMantRutas.getRowCount(); i++) {
            int fila = tblMantRutas.getSelectedRow();
            lblCodigo.setText(tblMantRutas.getValueAt(fila, 0).toString());
            lblCO.setText(tblMantRutas.getValueAt(fila, 1).toString());
            lblCD.setText(tblMantRutas.getValueAt(fila, 2).toString());
            cbTipoC.setSelectedItem(tblMantRutas.getValueAt(fila, 3).toString());
            txtDescripcion.setText(tblMantRutas.getValueAt(fila, 4).toString());

        }
    }//GEN-LAST:event_tblMantRutasMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        rpt = JOptionPane.showConfirmDialog(null, "Seguro que desea Actualizar?");

        if (rpt == JOptionPane.YES_OPTION) {
            rBean.setCiudadOrigen(lblCO.getText());
            rBean.setCiudadDestino(lblCD.getText());
            rBean.setTipoCircuito(cbTipoC.getSelectedItem().toString());
            rBean.setDescripcion(txtDescripcion.getText());
            rBean.setRutas(Integer.parseInt(lblCodigo.getText()));
            rDao.actualizarRuta(rBean);
            rDao.ListarMantenimientoRutas();
        } else if (rpt == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "No sea realizado ninguna Actualizacion");
        } else if (rpt == JOptionPane.CANCEL_OPTION) {
        }


    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        rpt = JOptionPane.showConfirmDialog(null, "Seguro que desea Eliminar?");

        if (rpt == JOptionPane.YES_OPTION) {
            rDao.eliminarRuta(String.valueOf(lblCodigo.getText()));
            rDao.ListarMantenimientoRutas();
        } else if (rpt == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "No sea realizado la accion");
        } else if (rpt == JOptionPane.CANCEL_OPTION) {

        }


    }//GEN-LAST:event_btnEliminarActionPerformed


