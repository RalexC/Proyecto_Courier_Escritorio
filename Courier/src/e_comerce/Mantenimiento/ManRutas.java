    private void cbTipoCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoCActionPerformed
        if (isSelected == cbTipoC.getSelectedItem().equals("L")) {
            lbltipoCircuito.setText("Circuito Largo");
            String nom1 = lblNomCO.getText();
            String nom2 = lblNomCD.getText();
            String nombre = nom1 + " - " + nom2;
            txtDescripcion.setText(nombre);
        } else if (isSelected == cbTipoC.getSelectedItem().equals("C")) {
            lbltipoCircuito.setText("Circuito Corto");
            String nom1 = lblNomCO.getText();
            String nom2 = lblNomCD.getText();
            String nombre = nom1 + " - " + nom2;
            txtDescripcion.setText(nombre);
        }
    }//GEN-LAST:event_cbTipoCActionPerformed

