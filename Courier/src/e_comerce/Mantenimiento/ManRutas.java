    private void btnverUbigeoOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnverUbigeoOrigenActionPerformed
        if (Principal.estacerrado(r)) {
            r = new Ubicacion();
            escritorio.add(r).setLocation(10, 3);
            r.toFront();
            r.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "La ventana Ubicacion\n ya esta abierta !!!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnverUbigeoOrigenActionPerformed

